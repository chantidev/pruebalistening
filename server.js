// ─── Get available languages only (fast, no subtitle fetching) ───
app.get('/api/languages', async (req, res) => {
  try {
    const videoId = extractVideoId(req.query.videoId);
    if (!videoId) return res.status(400).json({ error: 'URL o ID de video inválido' });

    const method = req.query.method || 'youtubei';
    console.log(`\n► [languages/${method}] ${videoId}`);

    if (method === 'supadata') {
      const result = await supadataFetchLangs(videoId);
      return res.json(result);
    }

    // youtubei.js path
    const yt = await getInnertube();
    const info = await yt.getInfo(videoId);

    let title = info.basic_info?.title || '';
    let author = info.basic_info?.author || '';
    const duration = info.basic_info?.duration || 0;

    // Fallback: oEmbed for title/author when InnerTube returns empty (cloud IP blocking)
    if (!title || !author) {
      try {
        const oembed = await fetch(`https://www.youtube.com/oembed?url=https://www.youtube.com/watch?v=${videoId}&format=json`).then(r => r.ok ? r.json() : {}).catch(() => ({}));
        title = title || oembed.title || 'Sin título';
        author = author || oembed.author_name || 'Desconocido';
      } catch { /* ignore */ }
    }

    let transcriptInfo;
    try {
      transcriptInfo = await info.getTranscript();
    } catch (firstErr) {
      try { transcriptInfo = await info.getTranscript(); } catch { /* no transcripts */ }
    }

    // caption_tracks: all available tracks including ASR (auto-generated)
    const captionTracks = info.captions?.caption_tracks || [];
    const rawLangs = transcriptInfo?.languages || [];
    const selectedLang = transcriptInfo?.selectedLanguage || '';

    console.log(`  caption_tracks: [${captionTracks.map(t => `${t.language_code}/${t.kind || 'manual'} "${t.name}"`).join(', ')}]`);
    console.log(`  transcript langs: [${rawLangs.join(', ')}] | selected: "${selectedLang}"`);

    const isAutoStr = s => typeof s === 'string' && (
      s.includes('auto-generated') || s.includes('generado') || s.includes('automático')
    );

    const seen = new Set();
    const languages = [];

    // 1. Primary: transcript panel language names (these work with selectLanguage())
    rawLangs.forEach(lang => {
      if (!seen.has(lang)) {
        seen.add(lang);
        languages.push({ languageCode: lang, name: lang, isAutoGenerated: isAutoStr(lang) });
      }
    });

    // 2. Fallback: selected language if not already listed
    if (selectedLang && !seen.has(selectedLang)) {
      seen.add(selectedLang);
      languages.push({ languageCode: selectedLang, name: selectedLang, isAutoGenerated: isAutoStr(selectedLang) });
    }

    // 3. Supplement: caption_tracks that aren't represented yet (especially ASR tracks)
    captionTracks.forEach(track => {
      const isAsr = track.kind === 'asr';
      const trackName = track.name?.toString?.() || track.language_code;
      const baseName = trackName.replace(/\s*\(.*\)/, '').trim().toLowerCase();

      // Check if this track type is already listed
      const alreadyListed = languages.some(l => {
        const lBase = l.name.replace(/\s*\(.*\)/, '').trim().toLowerCase();
        return lBase === baseName && l.isAutoGenerated === isAsr;
      });

      if (!alreadyListed) {
        const displayName = isAsr ? `${trackName} (auto-generated)` : trackName;
        if (!seen.has(displayName)) {
          seen.add(displayName);
          languages.push({ languageCode: displayName, name: displayName, isAutoGenerated: isAsr });
        }
      }
    });

    if (languages.length === 0) {
      // If caption_tracks is also empty, YouTube likely blocked this server's IP
      const blockedByYT = captionTracks.length === 0 && !transcriptInfo;
      const warning = blockedByYT
        ? 'youtubei.js no puede acceder a YouTube desde este servidor (IP de servidor cloud bloqueada). Usa el método Supadata.'
        : 'Este video no tiene subtítulos disponibles';
      return res.json({ videoId, title, author, duration, languages: [], warning });
    }

    res.json({ videoId, title, author, duration, languages });
  } catch (err) {
    console.error('[languages] Error:', err.message);
    res.status(500).json({ error: err.message });
  }
});

app.get('/api/subtitles', async (req, res) => {
  try {
    const videoId = extractVideoId(req.query.videoId);
    if (!videoId) {
      return res.status(400).json({ error: 'URL o ID de video inválido' });
    }

    console.log(`\n► Processing: ${videoId}`);
    const yt = await getInnertube();
    const info = await yt.getInfo(videoId);

    const videoData = {
      id: videoId,
      title: info.basic_info?.title || 'Sin título',
      author: info.basic_info?.author || 'Desconocido',
      duration: info.basic_info?.duration || 0,
      thumbnail: `https://i.ytimg.com/vi/${videoId}/hqdefault.jpg`,
      captionTracks: [],
      subtitles: {},
    };

    // Get transcript via engagement panel (POST /get_transcript, not timedtext GET)
    let transcriptInfo;
    try {
      transcriptInfo = await info.getTranscript();
    } catch (firstErr) {
      console.log(`  Retry getTranscript after error: ${firstErr.message}`);
      try {
        transcriptInfo = await info.getTranscript();
      } catch (err) {
        console.log(`  No transcript panel: ${err.message}`);
        return res.json({ ...videoData, warning: 'Este video no tiene subtítulos disponibles' });
      }
    }

    const availableLanguages = transcriptInfo.languages || [];
    const selectedLanguage = transcriptInfo.selectedLanguage || '';

    console.log(`  Languages: [${availableLanguages.join(', ')}]`);
    console.log(`  Default: "${selectedLanguage}"`);

    // Build caption tracks from available languages
    videoData.captionTracks = availableLanguages.map(lang => ({
      languageCode: lang,
      name: lang,
      kind: lang.includes('auto-generated') || lang.includes('generado') ? 'asr' : 'manual',
      isAutoGenerated: lang.includes('auto-generated') || lang.includes('generado'),
    }));

    // If a specific language is requested, fetch only that one
    const requestedLang = req.query.lang;
    const langsToFetch = requestedLang
      ? availableLanguages.filter(l => l === requestedLang)
      : availableLanguages;

    // Fetch default language segments
    if (!requestedLang || requestedLang === selectedLanguage) {
      const defaultSegments = extractSegments(transcriptInfo);
      videoData.subtitles[selectedLanguage] = { segments: defaultSegments, error: null };
      console.log(`  ✓ "${selectedLanguage}": ${defaultSegments.length} segments`);
    }

    // Fetch additional languages
    for (const lang of langsToFetch) {
      if (lang === selectedLanguage) continue;
      console.log(`  Fetching "${lang}"...`);
      try {
        let langTranscript;
        try {
          langTranscript = await transcriptInfo.selectLanguage(lang);
        } catch (firstErr) {
          // Try fuzzy match against available transcript panel languages
          const isAsr = lang.includes('auto-generated') || lang.includes('generado');
          const base = lang.replace(/\s*\(.*\)/, '').trim().toLowerCase();
          const match = availableLanguages.find(l => {
            const lBase = l.replace(/\s*\(.*\)/, '').trim().toLowerCase();
            const lIsAsr = l.includes('auto-generated') || l.includes('generado');
            return lBase === base && lIsAsr === isAsr;
          });
          if (match && match !== lang) {
            console.log(`  Fuzzy match "${lang}" → "${match}"`);
            langTranscript = await transcriptInfo.selectLanguage(match);
          } else {
            console.log(`  Retry "${lang}" after error: ${firstErr.message}`);
            langTranscript = await transcriptInfo.selectLanguage(lang);
          }
        }
        const segments = extractSegments(langTranscript);
        videoData.subtitles[lang] = { segments, error: null };
        console.log(`  ✓ "${lang}": ${segments.length} segments`);
      } catch (err) {
        console.error(`  ✗ "${lang}": ${err.message}`);
        videoData.subtitles[lang] = { segments: [], error: err.message };
      }
    }

    console.log(`  Done.`);
    res.json(videoData);
  } catch (err) {
    console.error('Error:', err.message);
    if (err.message?.includes('unavailable') || err.message?.includes('private')) {
      return res.status(404).json({ error: 'Video no disponible o es privado' });
    }
    res.status(500).json({ error: 'Error al procesar el video: ' + err.message });
  }
});

