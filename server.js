function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

async function supadataFetch(videoId, apiKey, lang = null) {
  const ytUrl = `https://www.youtube.com/watch?v=${videoId}`;
  const params = new URLSearchParams({ url: ytUrl, text: 'false' });
  if (lang) params.set('lang', lang);

  const response = await fetch(`https://api.supadata.ai/v1/transcript?${params}`, {
    headers: { 'x-api-key': apiKey },
  });

  if (!response.ok) {
    const body = await response.text();
    throw new Error(`Supadata ${response.status}: ${body}`);
  }

  const data = await response.json();

  // Handle async jobs
  if (data.jobId) {
    return await pollSupadataJob(data.jobId, apiKey);
  }

  return data;
}

async function pollSupadataJob(jobId, apiKey, maxAttempts = 30) {
  for (let i = 0; i < maxAttempts; i++) {
    await sleep(1000);
    const response = await fetch(`https://api.supadata.ai/v1/transcript/${jobId}`, {
      headers: { 'x-api-key': apiKey },
    });
    if (!response.ok) continue;
    const data = await response.json();
    if (data.status === 'completed') return data;
    if (data.status === 'failed') throw new Error(data.error || 'Job failed');
  }
  throw new Error('Supadata job timed out');
}

function supadataToSegments(content) {
  if (!Array.isArray(content)) return [];
  return content
    .filter(c => c.text?.trim())
    .map(c => {
      const startSec = (c.offset || 0) / 1000;
      const durSec = (c.duration || 0) / 1000;
      return {
        text: c.text.trim(),
        start: startSec,
        duration: durSec,
        startFormatted: formatTime(startSec),
        endFormatted: formatTime(startSec + durSec),
      };
    });
}

app.get('/api/subtitles-supadata', async (req, res) => {
  try {
    const videoId = extractVideoId(req.query.videoId);
    if (!videoId) {
      return res.status(400).json({ error: 'URL o ID de video inválido' });
    }

    const requestedLang = req.query.lang || null;
    const apiKey = SUPADATA_API_KEY;

    console.log(`\n► [Supadata] Processing: ${videoId} lang=${requestedLang || 'all'}`);

    // First call: get default language + available languages
    const defaultResult = await supadataFetch(videoId, apiKey);
    const defaultLang = defaultResult.lang || 'en';
    const availableLangs = defaultResult.availableLangs || [defaultLang];

    console.log(`  Default lang: ${defaultLang}`);
    console.log(`  Available: [${availableLangs.join(', ')}]`);

    const videoData = {
      id: videoId,
      title: '',
      author: '',
      duration: 0,
      thumbnail: `https://i.ytimg.com/vi/${videoId}/hqdefault.jpg`,
      captionTracks: availableLangs.map(lang => ({
        languageCode: lang,
        name: lang.toUpperCase(),
        kind: 'manual',
        isAutoGenerated: false,
      })),
      subtitles: {},
    };

    // Determine which languages to fetch
    const langsToFetch = requestedLang ? [requestedLang] : availableLangs;

    // Parse default language segments if needed
    if (langsToFetch.includes(defaultLang)) {
      const defaultSegments = supadataToSegments(defaultResult.content);
      videoData.subtitles[defaultLang] = { segments: defaultSegments, error: null };
      console.log(`  ✓ ${defaultLang}: ${defaultSegments.length} segments`);
    }

    // Fetch remaining languages
    for (const lang of langsToFetch) {
      if (lang === defaultLang) continue;
      console.log(`  Fetching ${lang}...`);
      try {
        const langResult = await supadataFetch(videoId, apiKey, lang);
        const segments = supadataToSegments(langResult.content);
        videoData.subtitles[lang] = { segments, error: null };
        console.log(`  ✓ ${lang}: ${segments.length} segments`);
      } catch (err) {
        console.error(`  ✗ ${lang}: ${err.message}`);
        videoData.subtitles[lang] = { segments: [], error: err.message };
      }
    }

    // Try to get video metadata
    try {
      const metaRes = await fetch(`https://www.youtube.com/oembed?url=https://www.youtube.com/watch?v=${videoId}&format=json`);
      if (metaRes.ok) {
        const meta = await metaRes.json();
        videoData.title = meta.title || '';
        videoData.author = meta.author_name || '';
      }
    } catch { /* ignore meta errors */ }

    console.log(`  Done.`);
    res.json(videoData);
  } catch (err) {
    console.error('[Supadata] Error:', err.message);
    res.status(500).json({ error: 'Error Supadata: ' + err.message });
  }
});

