let innertube = null;

async function getInnertube() {
  if (!innertube) {
    innertube = await Innertube.create();
  }
  return innertube;
}

function extractVideoId(input) {
  if (!input) return null;
  input = input.trim();

  if (/^[a-zA-Z0-9_-]{11}$/.test(input)) return input;

  try {
    const url = new URL(input);
    if (url.hostname.includes('youtube.com') && url.searchParams.has('v')) return url.searchParams.get('v');
    if (url.hostname === 'youtu.be') return url.pathname.slice(1).split('/')[0];
    const m = url.pathname.match(/\/(embed|v|shorts)\/([a-zA-Z0-9_-]{11})/);
    if (m) return m[2];
  } catch { /* not a URL */ }

  return null;
}

function formatTime(seconds) {
  const h = Math.floor(seconds / 3600);
  const m = Math.floor((seconds % 3600) / 60);
  const s = Math.floor(seconds % 60);
  const ms = Math.round((seconds % 1) * 1000);
  const msStr = String(ms).padStart(3, '0');
  if (h > 0) return `${h}:${String(m).padStart(2, '0')}:${String(s).padStart(2, '0')}.${msStr}`;
  return `${m}:${String(s).padStart(2, '0')}.${msStr}`;
}

function extractSegments(transcriptInfo) {
  const segments = [];
  const body = transcriptInfo.transcript?.content?.body;
  if (!body?.initial_segments) return segments;

  for (const seg of body.initial_segments) {
    if (seg.type !== 'TranscriptSegment') continue;

    const startMs = parseInt(seg.start_ms, 10) || 0;
    const endMs = parseInt(seg.end_ms, 10) || 0;
    const durationMs = endMs - startMs;
    const text = seg.snippet?.toString?.() || '';

    if (!text.trim()) continue;

    segments.push({
      text: text.trim(),
      start: startMs / 1000,
      duration: durationMs / 1000,
      startFormatted: seg.start_time_text?.toString?.() || formatTime(startMs / 1000),
      endFormatted: formatTime(endMs / 1000),
    });
  }

  return segments;
}

