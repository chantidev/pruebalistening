// ─── DOM refs ───
const searchForm      = document.getElementById('searchForm');
const videoInput      = document.getElementById('videoInput');
const extractBtn      = document.getElementById('extractBtn');
const loadingEl       = document.getElementById('loading');
const loadingText     = document.getElementById('loadingText');
const errorEl         = document.getElementById('error');
const langPickerEl    = document.getElementById('langPicker');
const resultsEl       = document.getElementById('results');
const warningEl       = document.getElementById('warning');
const thumbnailEl     = document.getElementById('thumbnail');
const videoTitleEl    = document.getElementById('videoTitle');
const videoAuthorEl   = document.getElementById('videoAuthor');
const videoDurationEl = document.getElementById('videoDuration');
const langListEl      = document.getElementById('langList');
const subtitleListEl  = document.getElementById('subtitleList');
const segmentCountEl  = document.getElementById('segmentCount');
const activeLangLabel = document.getElementById('activeLangLabel');
const backBtn         = document.getElementById('backBtn');
const copyBtn         = document.getElementById('copyBtn');
const copyTimestampsBtn = document.getElementById('copyTimestampsBtn');
const toastEl         = document.getElementById('toast');

// ─── State ───
let currentVideoId   = null;
let currentMethod    = 'youtubei';
let currentLangData  = null;
let currentSegments  = [];
let activeSegmentIdx = null;
let segmentTimer     = null;

// ─── YouTube IFrame Player ───
let ytPlayer     = null;
let ytPlayerReady = false;

window.onYouTubeIframeAPIReady = function () {
  ytPlayer = new YT.Player('ytPlayer', {
    width: '100%',
    height: '100%',
    playerVars: { rel: 0, modestbranding: 1 },
    events: {
      onReady: () => { ytPlayerReady = true; },
    },
  });
};

// ─── Form submit: Step 1 — fetch available languages ───
searchForm.addEventListener('submit', async (e) => {
  e.preventDefault();
  const input = videoInput.value.trim();
  if (!input) return;
  currentMethod = document.querySelector('input[name="method"]:checked').value;
  await fetchLanguages(input);
});

backBtn.addEventListener('click', () => {
  showStep('picker');
});

copyBtn.addEventListener('click', () => {
  if (!currentSegments.length) return;
  const text = currentSegments.map(s => s.text).join('\n');
  copyToClipboard(text, copyBtn);
});

copyTimestampsBtn.addEventListener('click', () => {
  if (!currentSegments.length) return;
  const text = currentSegments.map(s => `[${s.startFormatted}] ${s.text}`).join('\n');
  copyToClipboard(text, copyTimestampsBtn);
});

// ─── Step 1: fetch languages ───
async function fetchLanguages(videoIdInput) {
  showLoading(true, 'Buscando idiomas disponibles...');
  hideError();
  showStep(null);

  try {
    const url = `/api/languages?videoId=${encodeURIComponent(videoIdInput)}&method=${currentMethod}`;
    const res = await fetch(url);
    const data = await res.json();
    if (!res.ok) throw new Error(data.error || 'Error desconocido');

    currentVideoId = data.videoId;
    currentLangData = data;
    renderLangPicker(data);
  } catch (err) {
    showError(err.message);
  } finally {
    showLoading(false);
  }
}

function renderLangPicker(data) {
  thumbnailEl.src = `https://i.ytimg.com/vi/${data.videoId}/hqdefault.jpg`;
  thumbnailEl.alt = data.title;
  videoTitleEl.textContent  = data.title;
  videoAuthorEl.textContent = data.author;
  videoDurationEl.textContent = formatDuration(data.duration);

  if (!data.languages.length) {
    warningEl.textContent = 'Este video no tiene subtítulos disponibles';
    warningEl.classList.remove('hidden');
  } else {
    warningEl.classList.add('hidden');
  }

  langListEl.innerHTML = '';
  data.languages.forEach(lang => {
    const btn = document.createElement('button');
    btn.className = 'lang-btn';

    let label = escapeHtml(lang.name);
    if (lang.isAutoGenerated) {
      label = label.replace(/\s*\(auto-generated\)/i, '');
      label += ' <span class="auto-badge">auto</span>';
    }
    btn.innerHTML = `
      <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
        <path d="M21 15v4a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2v-4"/>
        <polyline points="7 10 12 15 17 10"/>
        <line x1="12" y1="15" x2="12" y2="3"/>
      </svg>
      <span>${label}</span>
    `;
    btn.addEventListener('click', () => fetchSubtitles(lang));
    langListEl.appendChild(btn);
  });

  showStep('picker');
}

// ─── Step 2: fetch subtitles for selected language ───
async function fetchSubtitles(lang) {
  showLoading(true, `Extrayendo subtítulos en ${lang.name}...`);
  showStep(null);

  try {
    const endpoint = currentMethod === 'supadata' ? '/api/subtitles-supadata' : '/api/subtitles';
    const url = `${endpoint}?videoId=${encodeURIComponent(currentVideoId)}&lang=${encodeURIComponent(lang.languageCode)}`;
    const res = await fetch(url);
    const data = await res.json();
    if (!res.ok) throw new Error(data.error || 'Error desconocido');

    const subs = data.subtitles[lang.languageCode];
    if (!subs || subs.error) throw new Error(subs?.error || 'Sin subtítulos para este idioma');

    currentSegments = subs.segments;
    activeLangLabel.textContent = lang.name;
    renderSubtitles(currentSegments);
    loadPlayer(currentVideoId);
    showStep('results');
  } catch (err) {
    showError(err.message);
    showStep('picker');
  } finally {
    showLoading(false);
  }
}

// ─── Render subtitle list ───
function renderSubtitles(segments) {
  segmentCountEl.textContent = `${segments.length} segmento${segments.length !== 1 ? 's' : ''}`;
  subtitleListEl.innerHTML = '';

  if (!segments.length) {
    subtitleListEl.innerHTML = `<div class="no-subs">No se encontraron subtítulos para este idioma</div>`;
    return;
  }

  const fragment = document.createDocumentFragment();
  segments.forEach((seg, idx) => {
    const div = document.createElement('div');
    div.className = 'subtitle-segment';
    div.dataset.idx = idx;
    div.title = 'Clic para reproducir este fragmento';
    div.innerHTML = `
      <span class="segment-time">${seg.startFormatted}</span>
      <span class="segment-text">${escapeHtml(seg.text)}</span>
      <svg class="play-icon" width="12" height="12" viewBox="0 0 24 24" fill="currentColor">
        <polygon points="5 3 19 12 5 21 5 3"/>
      </svg>
    `;
    div.addEventListener('click', () => playSegment(idx));
    fragment.appendChild(div);
  });

  subtitleListEl.appendChild(fragment);
}

// ─── Play a segment in the YouTube player ───
function playSegment(idx) {
  if (!ytPlayerReady || !ytPlayer) return;

  clearSegmentTimer();
  document.querySelectorAll('.subtitle-segment.playing').forEach(el => el.classList.remove('playing'));

  const seg = currentSegments[idx];
  activeSegmentIdx = idx;

  const el = subtitleListEl.querySelector(`[data-idx="${idx}"]`);
  if (el) {
    el.classList.add('playing');
    el.scrollIntoView({ behavior: 'smooth', block: 'nearest' });
  }

  ytPlayer.seekTo(seg.start, true);
  ytPlayer.playVideo();

  const durationMs = seg.duration * 1000;
  if (durationMs > 0) {
    segmentTimer = setTimeout(() => {
      ytPlayer.pauseVideo();
      el?.classList.remove('playing');
      activeSegmentIdx = null;
    }, durationMs + 150);
  }
}

function clearSegmentTimer() {
  if (segmentTimer) {
    clearTimeout(segmentTimer);
    segmentTimer = null;
  }
}

// ─── Load or update the YouTube player ───
function loadPlayer(videoId) {
  clearSegmentTimer();
  if (ytPlayerReady && ytPlayer) {
    ytPlayer.loadVideoById({ videoId, startSeconds: 0 });
    ytPlayer.pauseVideo();
  }
}

// ─── View management ───
function showStep(step) {
  langPickerEl.classList.toggle('hidden', step !== 'picker');
  resultsEl.classList.toggle('hidden', step !== 'results');
}

function showLoading(show, text = '') {
  loadingEl.classList.toggle('hidden', !show);
  extractBtn.disabled = show;
  if (text) loadingText.textContent = text;
}

function showError(message) {
  errorEl.textContent = message;
  errorEl.classList.remove('hidden');
}

function hideError() {
  errorEl.classList.add('hidden');
}

function formatDuration(seconds) {
  if (!seconds) return '';
  const h = Math.floor(seconds / 3600);
  const m = Math.floor((seconds % 3600) / 60);
  const s = Math.floor(seconds % 60);
  const parts = [];
  if (h > 0) parts.push(`${h}h`);
  if (m > 0) parts.push(`${m}m`);
  parts.push(`${s}s`);
  return parts.join(' ');
}

function escapeHtml(text) {
  const div = document.createElement('div');
  div.textContent = text;
  return div.innerHTML;
}

async function copyToClipboard(text, btn) {
  try {
    await navigator.clipboard.writeText(text);
    btn.classList.add('copied');
    showToast('Copiado al portapapeles');
    setTimeout(() => btn.classList.remove('copied'), 2000);
  } catch {
    showToast('Error al copiar');
  }
}

function showToast(message) {
  toastEl.textContent = message;
  toastEl.classList.remove('hidden');
  toastEl.classList.add('show');
  setTimeout(() => {
    toastEl.classList.remove('show');
    setTimeout(() => toastEl.classList.add('hidden'), 300);
  }, 2000);
}
